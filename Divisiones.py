print("EN PYTHON DIVIDIMOS CON EL OPERADOR: '/'")
num1 = 16
num2 = 3
resultado = num1 / num2
print("Resultado:", resultado)

print("EN PYTHON OBTENEMOS EL COCIENTE CON EL OPERADOR: '//'")

resultado = num1 // num2
print("Resultado (Cociente):", resultado)

print("EN PYTHON OBTENEMOS EL RESTO CON EL OPERADOR: '%'")

resultado = num1 % num2
print("Resultado (Resto):", resultado)

